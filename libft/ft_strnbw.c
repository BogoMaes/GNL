#include "libft.h"

size_t		ft_strnbw(const char *s, char c)
{
	size_t	i;
	size_t	nb;

	nb = 0;
	i = 0;
	if (s != NULL)
	{
		while (s[i])
		{
			if (s[i] != c && s[i - 1] == c)
				nb++;
			i++;
		}
		return (nb + 1);
	}
	return (0);
}
